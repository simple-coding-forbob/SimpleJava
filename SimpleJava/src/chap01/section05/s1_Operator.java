package chap01.section05;

/**
 * @author simple-coding
 *
 */
public class s1_Operator {
	public static void main(String[] args) {
		int x = 10;
		int y = 10;
		
		x++;
		System.out.println(x);
		++x;
		System.out.println(x);
		
		y--;
		System.out.println(y);
		--y;
		System.out.println(y);
	}
}
