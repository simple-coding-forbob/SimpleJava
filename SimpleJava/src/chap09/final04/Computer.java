package chap09.final04;

public class Computer {

    void turnOn() {
        System.out.println("전원을 켭니다.");
    }

    void displayOn(){
        System.out.println("화면을 켭니다.");
    }
    void typeOn(){
        System.out.println("타이핑을 합니다.");
    }

    void turnOff(){
        System.out.println("전원을 끕니다.");
    }
}
